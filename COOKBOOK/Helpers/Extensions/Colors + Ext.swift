import Foundation
import UIKit

extension UIColor {
    class func CustomGreen() -> UIColor {
        return #colorLiteral(red: 0.1098039216, green: 0.6196078431, blue: 0.0431372549, alpha: 1)
    }
    
    class func LightGreen() -> UIColor {
        return #colorLiteral(red: 0.6156862745, green: 0.8470588235, blue: 0.07450980392, alpha: 1)
    }
    
    class func customDarkGray() -> UIColor {
        return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    
    class func customLightGray() -> UIColor {
        return #colorLiteral(red: 0.4, green: 0.4, blue: 0.4, alpha: 1)
    }
}
